/** Vrátí unikátní skupiny modelů pro danou kategorii v pořadí, v jakém se objevují v datech. */
export function getGroups(category: DeviceCategory): string[] {
  const seen = new Set<string>();
  const groups: string[] = [];
  for (const model of repairData[category]) {
    if (!seen.has(model.group)) {
      seen.add(model.group);
      groups.push(model.group);
    }
  }
  return groups;
}

/** Formátuje cenu: 1490 → "1 490 Kč" */
export function formatPrice(price: number): string {
  return price.toLocaleString('cs-CZ') + ' Kč';
}
